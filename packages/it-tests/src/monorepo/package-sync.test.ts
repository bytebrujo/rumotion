import {expect, test} from 'bun:test';
import {writeFileSync} from 'node:fs';
import path from 'node:path';
import {CliInternals} from '@picus/cli';
import {packages} from '@picus/studio-shared';
import {CreateVideoInternals} from 'create-video';

test('create-video and cli should have the same list of packages', () => {
	const list1 = path.resolve(
		__dirname,
		'..',
		'..',
		'..',
		'create-video',
		'src',
		'list-of-picus-packages.ts',
	);
	const list2 = path.resolve(
		__dirname,
		'..',
		'..',
		'..',
		'cli',
		'src',
		'list-of-picus-packages.ts',
	);
	const contents =
		[
			'// Generated by package-sync.test.ts',
			'export const listOfPicusPackages = [',
			...packages
				.map((p) => (p === 'core' ? 'picus' : `@picus/${p}`))
				.map((p) => `\t'${p}',`),
			'];',
		].join('\n') + '\n';

	writeFileSync(list1, contents);
	writeFileSync(list2, contents);

	expect(CreateVideoInternals.listOfPicusPackages).toEqual(
		CliInternals.listOfPicusPackages,
	);
});
